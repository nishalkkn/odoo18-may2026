from odoo import fields, models


class TagColor(models.Model):
    """Represents a color tag that can be assigned to various records for visual categorization
    or status indication. Each tag color has a name and a color index to uniquely identify and
    apply the color within the system."""
    _name = "tag.color"
    _description = "Tag Colors"

    name = fields.Char(string="Name")
    color = fields.Char(string='Color Index')
